#include "ft_printf.h"

wchar_t			*ft_fill_null(wchar_t *str)
{
	str = (wchar_t *)malloc(sizeof(wchar_t) * 7);
	str[0] = '(';
	str[1] = 'n';
	str[2] = 'u';
	str[3] = 'l';
	str[4] = 'l';
	str[5] = ')';
	str[6] = '\0';
	return (&str[0]);
}
