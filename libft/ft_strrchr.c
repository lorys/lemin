#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	int i;

	i = ft_strlen((char *)s);
	while (s[i] != (char)c && i > 0)
		i--;
	return ((s[i] == (char)c) ? (char *)s + i : NULL);
}
