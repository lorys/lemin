#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int i;

	i = 0;
	while (s[i] != (char)c && i < (int)ft_strlen((char *)s))
		i++;
	return ((s[i] == (char)c) ? (char *)s + i : NULL);
}
